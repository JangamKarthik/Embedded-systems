	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	mov R0,#5
	LDR R1,=RES
	MOV R2,#6
	MOV R3,#0
	MLA R4,R0,R2,R3
	MOVS R5,R4,LSR #1
	STR R5,[R1]
STOP
	B STOP
	AREA DATASEG,DATA,READWRITE
RES DCD 0
	END