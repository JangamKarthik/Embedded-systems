	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R0,#10
	MOV R1,#6
	MUL R3,R0,R1
	LDR R2,=RES
LOOP
	CMP R0,R1
	BHI TRUE
	BLS FALSE
TRUE 
	SUB R0,R0,R1
	B NXT
FALSE
	SUB R1,R1,R0
NXT
	CMP R0,R1
	BNE LOOP
	MOV R4,#0
XYZ
	CMP R3,R0
	BCC LAST
	SUB R3,R3,R0
	ADD R4,#1
	CMP R3,R0
	BNE XYZ
LAST
	STR R4,[R2]
STOP
	B STOP
	AREA DATASEG,DATA,READWRITE
RES DCD 0
	END