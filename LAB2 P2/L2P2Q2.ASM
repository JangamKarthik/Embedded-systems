	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R0,#10
	MOV R1,#6
	LDR R3,=RES
LOOP
	CMP R0,R1
	BHI TRUE
	BLS FALSE
TRUE 
	SUB R0,R0,R1
	B NXT
FALSE
	SUB R1,R1,R0
NXT
	CMP R0,R1
	BNE LOOP
	STR R0,[R3]
STOP
	B STOP
	AREA DATASEG,DATA,READWRITE
RES DCD 0
	END